
public class penClass {
	
	String color;
	String type;
	
	public penClass(String color, String type) {
		super();
		this.color = color;
		this.type = type;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
